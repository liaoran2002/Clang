/*
16进制的简单运算
现在给你一个16进制的加减法的表达式，
要求用8进制输出表达式的结果。

输入
输入一个两个数和一个加号或者一个减号，
且表达式合法并且所有运算的数都小于31位

输出
输出表达式8进制的结果。

strchr函数的用法：
参数s为要搜索的字符串，参数"+"为要搜索的字符。
strchr(s,'+')返回字符串s中第一个出现的加号的位置，
如果没有则返回NULL。

strcpy函数的用法：
strcpy(a,b)
参数a为目标字符串，参数b为源字符串。
将字符串b的内容复制到字符串a中。

strtok函数的用法：
strtok(s,"+")
参数s为要分割的字符串，参数"+"为分隔符。
将字符串s中第一个出现的加号后面的内容分割成多个字符串，
并返回第一个字符串的地址。'

strtol函数的用法：
strtol(s,NULL,16)
参数s为要转换的字符串，参数NULL为分隔符，16为进制。
将字符串s转换为16进制整数，并返回整数值。
*/
#include <stdio.h>

int main() {
    int a, b, sum;
    char op;
    printf("请输入两个16进制数和一个运算符(+/-):");
    scanf("%x %c %x", &a, &op, &b);
    if (op == '+') {
        sum = a + b;
    } else if (op == '-') {
        sum = a - b;
    } else {
        printf("输入的运算符有误！\n");
        return 0;
    }
    printf("8进制结果为:%o\n", sum);
    return 0;
}